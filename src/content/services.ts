export function findHub(slug: string): ServiceHub | undefined {
  return SERVICES.find((s) => s.slug === slug);
}

export function findChild(
  hubSlug: string,
  childSlug: string,
): { hub: ServiceHub; child: ChildPage } | undefined {
  const hub = findHub(hubSlug);
  if (!hub) return undefined;
  const child = hub.children.find((c) => c.slug === childSlug);
  if (!child) return undefined;
  return { hub, child };
}